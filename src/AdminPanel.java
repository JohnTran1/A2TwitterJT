    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (currentSelectionString == null) {
            JOptionPane.showMessageDialog(null, "Select a group or user to view information");
        }

        if (Admin.getInstance().userExists(currentSelectionString)) {
            UserPanel view = new UserPanel((User) Admin.getInstance().getUser(currentSelectionString));
            view.setVisible(true);
        } else if (Admin.getInstance().groupExists(currentSelectionString)) {
            JOptionPane.showMessageDialog(null, "GroupID: " + currentSelectionString + "- created on " + Admin.getInstance().getTreeRoot().getGroup(currentSelectionString).getCreationTime());
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTextField1.getText() != null && !"".equals(jTextField1.getText())) {
            if (Admin.getInstance().groupExists(currentSelectionString)) {
                Admin.getInstance().addUser(currentSelectionString, jTextField1.getText());
                jTree1.setModel(Admin.getInstance().getTree());
                expandTree();
                jTextField1.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Select a group from the tree to add this user to");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

